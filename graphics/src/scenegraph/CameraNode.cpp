#include <storm/graphics/scenegraph/CameraNode.hpp>
#include <storm/graphics/scenegraph/SceneRenderer.hpp>

using namespace storm;
using namespace storm::graphics;

////////////////////////////////////////
////////////////////////////////////////
CameraNode::CameraNode(std::string name) : Node{"CameraNode"} {
	setName(std::move(name));
}

////////////////////////////////////////
////////////////////////////////////////
CameraNode::~CameraNode() = default;

////////////////////////////////////////
////////////////////////////////////////
CameraNode::CameraNode(CameraNode &&) = default;

////////////////////////////////////////
////////////////////////////////////////
CameraNode &CameraNode::operator=(CameraNode &&) = default;

////////////////////////////////////////
////////////////////////////////////////
void CameraNode::visit(SceneVisitorContext &context,
					   SceneRenderer &renderer) const {
	renderer.visit(context, *this);
}
